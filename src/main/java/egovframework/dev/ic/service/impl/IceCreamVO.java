package egovframework.dev.ic.service.impl;

public class IceCreamVO {


    private String icNo;
    private String icNm;
    private String icPrice;
    private String icComp;
    private String icDate;
    private String sc;
    private String searchKeyword;

    public String getSc() {
        return sc;
    }

    public void setSc(String sc) {
        this.sc = sc;
    }

    public String getSearchKeyword() {
        return searchKeyword;
    }

    public void setSearchKeyword(String searchKeyword) {
        this.searchKeyword = searchKeyword;
    }

    public String getIcNo() {
        return icNo;
    }

    public void setIcNo(String icNo) {
        this.icNo = icNo;
    }

    public String getIcNm() {
        return icNm;
    }

    public void setIcNm(String icNm) {
        this.icNm = icNm;
    }

    public String getIcPrice() {
        return icPrice;
    }

    public void setIcPrice(String icPrice) {
        this.icPrice = icPrice;
    }

    public String getIcComp() {
        return icComp;
    }

    public void setIcComp(String icComp) {
        this.icComp = icComp;
    }

    public String getIcDate() {
        return icDate;
    }

    public void setIcDate(String icDate) {
        this.icDate = icDate;
    }
}
